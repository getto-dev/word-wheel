export const ALL_WORDS: string[] = Object.values(DICTIONARY).flat();

// Получить слова по длине
export function getWordsByLength(length: number): string[] {
  return DICTIONARY[length] || [];
}

// Получить случайное слово определённой длины
export function getRandomWord(length: number): string {
  const words = DICTIONARY[length];
  if (!words || words.length === 0) return '';
  return words[Math.floor(Math.random() * words.length)];
}

// Найти все слова, которые можно составить из данных букв
export function findWordsFromLetters(letters: string, minLength: number = 3): string[] {
  const letterCount: Record<string, number> = {};
  for (const letter of letters.toUpperCase()) {
    letterCount[letter] = (letterCount[letter] || 0) + 1;
  }
  
  const result: string[] = [];
  
  for (const word of ALL_WORDS) {
    if (word.length < minLength || word.length > letters.length) continue;
    
    const wordLetterCount: Record<string, number> = {};
    for (const letter of word) {
      wordLetterCount[letter] = (wordLetterCount[letter] || 0) + 1;
    }
    
    let canForm = true;
    for (const [letter, count] of Object.entries(wordLetterCount)) {
      if ((letterCount[letter] || 0) < count) {
        canForm = false;
        break;
      }
    }
    
    if (canForm) {
      result.push(word);
    }
  }
  
  return result.sort((a, b) => a.length - b.length);
}
